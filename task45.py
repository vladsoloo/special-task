num1 = int(input("Введите num1: "))
num2 = int(input("Введите num2: "))


sum = num1 + num2
difference = num1 - num2
product = num1 * num2
quotient = round(num1 / num2, 2)
print("Сумма равно: ", sum)
print("Разность равна: ", difference)
print("Произвнедение равно: ", product)
print("Частное равно: ", quotient)
